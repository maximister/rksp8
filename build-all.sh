#!/bin/bash

# Скрипт для сборки всех модулей проекта
# Использование: ./build-all.sh

echo "======================================"
echo "Сборка проекта"
echo "======================================"
echo ""

# Переходим в корневую директорию
cd "$(dirname "$0")"

# Собираем весь проект из корня
echo "Сборка всех модулей..."
mvn clean install -DskipTests

if [ $? -eq 0 ]; then
    echo ""
    echo "======================================"
    echo "✓ Сборка успешно завершена!"
    echo "======================================"
    echo ""
    echo "Теперь можете запустить сервисы:"
    echo "  ./start-all.sh"
    echo ""
else
    echo ""
    echo "======================================"
    echo "✗ Ошибка при сборке проекта"
    echo "======================================"
    echo ""
    exit 1
fi




